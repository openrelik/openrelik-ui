<template>
  <v-dialog v-model="showNewFolderDialog" width="400">
    <v-card width="400" class="mx-auto">
      <v-card-title>New folder</v-card-title>
      <div class="pa-4">
        <v-form @submit.prevent @keyup.enter="createFolder()">
          <v-text-field
            v-model="newFolderForm.name"
            variant="outlined"
            label="Folder name"
            autofocus
          ></v-text-field>
        </v-form>
        <v-btn
          variant="text"
          color="primary"
          class="text-none"
          @click="createFolder()"
          >Create</v-btn
        >
        <v-btn
          variant="text"
          class="text-none"
          @click="showNewFolderDialog = false"
          >Cancel</v-btn
        >
      </div>
    </v-card>
  </v-dialog>

  <v-card variant="flat" color="transparent">
    <v-row align="center">
      <v-col cols="auto">
        <v-btn
          variant="outlined"
          class="text-none custom-border-color mt-2"
          prepend-icon="mdi-folder-plus-outline"
          @click="showNewFolderDialog = true"
          >New folder</v-btn
        >
      </v-col>
      <v-col>
        <v-tabs v-model="activeTab">
          <v-tab
            v-for="tab in tabs"
            :key="tab.value"
            :value="tab.value"
            class="text-none"
            :prepend-icon="tab.icon"
            @click="updateRoute(tab.routeName)"
            >{{ tab.name }}</v-tab
          >
        </v-tabs>
      </v-col>
    </v-row>
    <br />
  </v-card>

  <v-tabs-window v-model="activeTab">
    <v-tabs-window-item
      :value="1"
      :transition="false"
      :reverse-transition="false"
    >
      <folder-list
        :items="folders"
        :is-home-view="true"
        @folder-deleted="removeFolder($event)"
      ></folder-list>
    </v-tabs-window-item>
    <v-tabs-window-item
      :value="2"
      :transition="false"
      :reverse-transition="false"
    >
      <folder-list
        :items="folders"
        :is-home-view="true"
        @folder-deleted="removeFolder($event)"
      ></folder-list>
    </v-tabs-window-item>
  </v-tabs-window>
</template>

<script>
import RestApiClient from "@/RestApiClient";
import FolderList from "@/components/FolderList";
import UploadFile from "@/components/UploadFile";

export default {
  name: "home",
  components: {
    FolderList,
    UploadFile,
  },
  data() {
    return {
      folders: [],
      showNewFolderDialog: false,
      newFolderForm: {
        name: "",
      },
      activeTab: null,
      tabs: [
        {
          name: "My Folders",
          value: 0,
          routeName: "myFolders",
          route: "my-folders",
          icon: "mdi-folder-multiple-outline",
        },
        {
          name: "Shared With Me",
          value: 1,
          routeName: "sharedWithMe",
          route: "shared-with-me",
          icon: "mdi-folder-account-outline",
        },
      ],
    };
  },
  computed: {},
  methods: {
    createFolder() {
      if (this.newFolderForm.name === "") {
        return;
      }
      RestApiClient.createFolder(this.newFolderForm.name).then((response) => {
        this.showNewFolderDialog = false;
        this.newFolderForm.name = "";
        this.folders.unshift(response);
        this.$router.push({
          name: "folder",
          params: { folderId: response.id },
        });
      });
    },
    removeFolder(folder_to_remove) {
      this.folders = this.folders.filter(
        (folder) => folder.id != folder_to_remove.id
      );
    },
    getFolders() {
      this.folders = [];
      if (this.activeTab === 0) {
        RestApiClient.getRootFolders().then((response) => {
          this.folders = response;
        });
      } else if (this.activeTab === 1) {
        RestApiClient.getSharedFolders().then((response) => {
          this.folders = response;
        });
      }
    },
    updateRoute(routeName) {
      this.$router.replace({
        name: routeName,
      });
      this.getFolders();
    },
    setActiveTab() {
      const tabIndex = this.tabs.findIndex(
        (tab) => tab.routeName === this.$route.name
      );
      this.activeTab = tabIndex;
      this.getFolders();
    },
  },

  mounted() {
    this.setActiveTab();
    // If there is a URL parameter 'redirect' then redirect the user to it. This will
    // preserve any URL that the user clicked when not authenticated. When the user has
    // authenticated they will be redirected to the original location they were trying
    // to access. The 'redirect' paramter is set in the Login view.
    const redirect_url = sessionStorage.getItem("redirect_url");
    if (redirect_url) {
      this.$router.replace(redirect_url);
      sessionStorage.removeItem("redirect_url");
    }
  },
};
</script>
